var a = 0;
      function increase(){
            var textBox = document.getElementById("txtvalue");
            textBox.value = a+1;
            a++;
      }
	  
	  function decrease() {
	  if(a!=0)
	  {
	  	var textbox=document.getElementById("txtvalue");
	  	textbox.value=a-1;
	  	a--;
	  }
	  } 
